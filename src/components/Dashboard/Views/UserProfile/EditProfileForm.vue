                    placeholder="Light dashboard"
                    v-model="user.account_type">
          </fg-input>
        </div>

        <div class="col-md-6">
          <fg-input type="email"
                    label="Email"
                    placeholder="Email"
                    v-model="user.email">
          </fg-input>
        </div>
      </div>

      <div class="row">
        <div class="col-md-6">
          <fg-input type="text"
                    label="First Name"
                    placeholder="First Name"
                    v-model="user.first_name">
          </fg-input>
        </div>
        <div class="col-md-6">
          <fg-input type="text"
                    label="Last Name"
                    placeholder="Last Name"
                    v-model="user.last_name">
          </fg-input>
        </div>
      </div>
	  
	  <div class="row">
	    <div class="col-md-6">
		  <fg-input type="text"
					label="Change Password"
					>
		  </fg-input>
		</div>
		
		<div class="col-md-6">
		  <fg-input type="text"
					label="Confirm Password"
					>
		  </fg-input>
		</div>
	  </div>

      <div class="row">
        <div class="col-md-4">
          <div class="form-group">
            <label class="control-label">
              Undergraduate Degree
            </label>
            <select v-model="user.undergraduate_degree" class="form-control">
              <option value="Empty"></option>
              <option v-for="item in options.undergraduate_degree" v-bind:value="item">{{item}}</option>
            </select>
          </div>
        </div>
        <div class="col-md-4">
          <div class="form-group">
            <label class="control-label">
              Graduate Degree
            </label>
            <select v-model="user.graduate_degree" class="form-control">
              <option value="Empty"></option>
              <option v-for="item in options.graduate_degree" v-bind:value="item">{{item}}</option>
            </select>
          </div>
        </div>
        <div class="col-md-4">
          <div class="form-group">
            <label class="control-label">
              Graduate Degree Concentration
            </label>
            <select v-model="user.graduate_degree_concentration" class="form-control">
                <option value="Empty"></option>
