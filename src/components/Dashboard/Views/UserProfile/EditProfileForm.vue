          <div>
            <input type="radio" v-model="user.automation" value="Work collaboratively with the system to choose my classes" /> Work collaboratively with the system to choose my classes</br>
            <input type="radio" v-model="user.automation" value="Have my class list generated by the system" /> Have my class list generated by the system</br>
          </div>
        </div>
      </div>

      <div class="text-center">
        {{ errorMessage }}
        <button type="submit" class="btn btn-info btn-fill float-right" @click.prevent="updateProfile">
