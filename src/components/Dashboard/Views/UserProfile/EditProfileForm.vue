        <div class="col-md-3">
          <label class="control-label">
            Electives
          </label>
          <div>
            <input type="radio" v-model="user.elective" value="Choose my own electives" /> Choose my own electives</br>
            <input type="radio" v-model="user.elective" value="Auto-select my electives" /> Auto-select my electives</br>
          </div>
        </div>
        <div class="col-md-6">
          <label class="control-label">
            Automation
